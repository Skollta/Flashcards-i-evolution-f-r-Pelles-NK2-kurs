function loadFlashcards() {
    const flashcardContainer = document.getElementById("flashcardContainer");
    const lines = questionData.split('\n');
    
    lines.forEach(line => {
        const [question, answer] = line.split(';');
        
        if (question && answer) {
            const flashcard = document.createElement('div');
            flashcard.classList.add('flashcard');
            
            // Lägg till en klick-händelse för att vända kortet
            flashcard.onclick = function() {
                flashcard.classList.toggle("flipped");
            };

            const front = document.createElement('div');
            front.classList.add('front');
            front.textContent = question.trim();

            const back = document.createElement('div');
            back.classList.add('back');
            back.textContent = answer.trim();

            flashcard.appendChild(front);
            flashcard.appendChild(back);
            flashcardContainer.appendChild(flashcard);
        }
    });
}

function addCard(event) {
    event.preventDefault();
    const front = document.getElementById("front").value;
    const back = document.getElementById("back").value;

    const flashcardContainer = document.getElementById("flashcardContainer");
    
    const flashcard = document.createElement('div');
    flashcard.classList.add('flashcard');
    
    flashcard.onclick = function() {
        flashcard.classList.toggle("flipped");
    };

    const frontDiv = document.createElement('div');
    frontDiv.classList.add('front');
    frontDiv.textContent = front;

    const backDiv = document.createElement('div');
    backDiv.classList.add('back');
    backDiv.textContent = back;

    flashcard.appendChild(frontDiv);
    flashcard.appendChild(backDiv);
    flashcardContainer.appendChild(flashcard);

    document.getElementById("add-card-form").reset();
}

document.getElementById("add-card-form").addEventListener("submit", addCard);
document.addEventListener('DOMContentLoaded', loadFlashcards);